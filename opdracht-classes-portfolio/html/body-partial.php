        <p>Deze imponerende, koninklijke roofvogel is moedig en sluw. Hij heeft een grenzeloos geduld. De Arend is goed ontwikkeld, zowel op lichamelijk als geestelijk vlak. Hij heeft een fiere en kalme aard. De Arend is gevoelig en zal steeds duidelijk zijn ongenoegen laten blijken. Hij volhardt hardnekkig in een eens opgevat plan. Zelfs door tegenstand laat hij zich niet van zijn voornemen afbrengen. De Arend is leergierig, trots en waardig.</p>
